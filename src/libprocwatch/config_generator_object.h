#ifndef __CONFIG_GENERATOR_H__
#define __CONFIG_GENERATOR_H__

#include "bhyve_config.h"

/*
 * Defines a configuration generator, that merges a bhyve config file and settings
 * in a bhyve_configuration into a new bhyve_config file
 */
struct config_generator_object {
	/*
	 * Conversion method
	 *
	 * returns 0 on success
	 */
	int(*generate_config_file)(const struct bhyve_configuration *bc, const char *filename);
};

#endif /* __CONFIG_GENERATOR_H__ */
